a = 5
b = 4
print(a & b) #bitwise and operator

print(a | b) #bitwise or operator

print(a ^ b) #bitwise xor operator (0&0=0, 1&1=0)

print(~a) #bitwise not(complement(2's)) operator

## ~10 It's output = -(10+1) = -11

print(a << 2) # Left shift bitwise operator(here is gain bit or increase value bits)
## x << n = x * 2^n (v.v.I laws)

print(a >> 2) # Right shift bitwise operator(here is bit discard or delete)
## x >> n = x / 2^n


